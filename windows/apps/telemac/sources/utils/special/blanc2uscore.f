!                    *****************
      SUBROUTINE BLANC2USCORE(STRING,N)
!                    *****************
!***********************************************************************
! SPECIAL V7P2
!***********************************************************************
!
!brief    Replance space by underscore in string
!
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!| STRING         |<->| THE CHARACTER ARRAY TO CHANGE
!| N              |-->| SIZE OF STRING
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
      USE DECLARATIONS_SPECIAL
!
      IMPLICIT NONE
!
!+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
!
      INTEGER, INTENT(IN) :: N
      CHARACTER(LEN=N), INTENT(INOUT) :: STRING
!
!+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
!
      INTEGER :: I
!
!-----------------------------------------------------------------------
!
      DO I=1,N
        IF(STRING(I:I).EQ.' ') STRING(I:I) = '_'
      ENDDO
!
!-----------------------------------------------------------------------
!
      END SUBROUTINE BLANC2USCORE


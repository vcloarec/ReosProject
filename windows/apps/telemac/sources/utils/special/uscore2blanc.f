!                    *****************
      SUBROUTINE USCORE2BLANC(STRING,N)
!                    *****************
!***********************************************************************
! SPECIAL V7P2
!***********************************************************************
!
!brief    Replance underscore by space in string
!
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!| STRING         |<->| THE CHARACTER ARRAY TO CHANGE
!| N              |-->| SIZE OF STRING
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
      USE DECLARATIONS_SPECIAL
!
      IMPLICIT NONE
!
!+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
!
      CHARACTER(LEN=*), INTENT(INOUT) :: STRING
      INTEGER, INTENT(IN) :: N
!
!+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
!
      INTEGER :: I
!
!-----------------------------------------------------------------------
!
      DO I=1,N
        IF(STRING(I:I).EQ.'_') STRING(I:I) = ' '
      ENDDO
!
!-----------------------------------------------------------------------
!
      END SUBROUTINE USCORE2BLANC

